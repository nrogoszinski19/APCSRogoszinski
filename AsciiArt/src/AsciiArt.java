		System.out.println("    jo  o    |");
		System.out.println("   ( ~T~     j");
		System.out.println("    >._-' _./");
		System.out.println("   /   \"-\"  |");
		System.out.println("  Y     _,  |");
		System.out.println(" /| ;-\"- _  |");
		System.out.println("/ |/ ,-\"-    \\");
		System.out.println("\\//\\/      .- \\");
