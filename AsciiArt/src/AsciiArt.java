		System.out.println("         ,");
		System.out.println("        /|      __");
		System.out.println("       / |   ,-- /");
		System.out.println("      Y :|  //  /");
		System.out.println("      | :| //  ^");
		System.out.println("      >-\"-\"-v\"/");
