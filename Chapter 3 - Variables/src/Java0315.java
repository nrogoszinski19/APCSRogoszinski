// Java0315.java
// This program demonstrates the <boolean> data type.
// The boolean type can only have two values: true or false.


public class Java0315
{
	public static void main (String args[])
	{
		boolean value = true;
		System.out.println("value: " + value);
		value = false;
		System.out.println("value: " + value);
		System.out.println();
   }
}

