// Java0305.java
// This program demonstrates the five integer operations.


public class Java0305
{

	public static void main (String args[])
	{
		int a = 0;
		int b = 25;
		int c = 10;

		a = b + c;										// Addition
		System.out.println(b + " + " + c + " = " + a);

		a = b - c;										// Subtraction
		System.out.println(b + " - " + c + " = " + a);

		a = b * c;										// Multiplication
		System.out.println(b + " * " + c + " = " + a);

		a = b / c;										// Integer Division
		System.out.println(b + " / " + c + " = " + a);

		a = b % c;										// Remainder Division
		System.out.println(b + " % " + c + " = " + a);
	}

}

